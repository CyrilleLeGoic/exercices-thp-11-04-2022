puts "Travail : #{10 * 5 * 11}" #Permet d'envoyer une fonction string incluant une opération 10x5x11 grace à #()
puts "En minutes ça fait : #{10 * 5 * 11 * 60}" #Permet d'envoyer une fonction string incluant une opération 10x5x11x60 grace à #()

puts "Et en secondes ?" #Permet d'envoyer une fonction string

puts 10 * 5 * 11 * 60 * 60 #Permet de réaliser une opération

